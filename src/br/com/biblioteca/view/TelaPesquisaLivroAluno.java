    private void btnInformacoesActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnInformacoesActionPerformed
        // TODO add your handling code here:
        int linhaSelecionada = tabelaListaLivro.getSelectedRow();
        if(linhaSelecionada == -1){
            JOptionPane.showMessageDialog(this, "Nenhum Livro Selecionado", "Falha ao Ver as Informações", JOptionPane.WARNING_MESSAGE);
            return;
        }
        Livro livro = new Livro();
        try{
            Object obj = tabelaListaLivro.getValueAt(linhaSelecionada, 2);
            String isbn = String.valueOf(obj);
            livro = livros.getLivroIsbn(isbn);
            TelaInfoEditLivro telaInfoEditLivro = new TelaInfoEditLivro(this.parent, false, livro, false);
            telaInfoEditLivro.setVisible(true);
            telaInfoEditLivro.setLocationRelativeTo(null);
            dispose();
        }catch(NullPointerException ex){
            JOptionPane.showMessageDialog(this, "Erro ao Selecionar o Livro", "Erro", JOptionPane.ERROR_MESSAGE);
        }

    }//GEN-LAST:event_btnInformacoesActionPerformed

