    public void inserirLinhaTabela(Livro livro, DefaultTableModel tabela){  
        tabela.addRow(new String[] {livro.getTitulo(), livro.getAutor(), livro.getIsbn(), String.valueOf(livro.getQuantidade()), String.valueOf(livro.getQuantidadeDisponivel())});
    }
    
    public void limparTabela(){
        DefaultTableModel tabela = (DefaultTableModel) tabelaListaLivro.getModel();
        while(tabela.getRowCount() > 0){
            tabela.removeRow(0);
        }
    }
    
    private void removerLivro(int index){
        int decisaodeExcluir = 0;
        Livro livro = new Livro();
           try{
                Object obj = tabelaListaLivro.getValueAt(index, 2);
                String isbn = String.valueOf(obj);
                livro = livros.getLivroIsbn(isbn);
                decisaodeExcluir = JOptionPane.showOptionDialog(this, "Deseja Realmente Excluir o Livro ", "Decisão de Exclusão", JOptionPane.YES_OPTION, JOptionPane.NO_OPTION, null, null, null);
                if(decisaodeExcluir == 0){
                    livros.removerLivro(livro);
                    JOptionPane.showMessageDialog(this, "Livro Excluido", "Exclusão Efetuada", JOptionPane.INFORMATION_MESSAGE);
                }
           }catch(NullPointerException ex){
               JOptionPane.showMessageDialog(this, "Erro ao Realizar a Exclusão", "Erro", JOptionPane.ERROR_MESSAGE);
           }

           
    }
    
    private void buscarLivro(boolean buscar){
        boolean flagEncontrouLivro = false;
        Livro livro = new Livro();
        String txtDeBusca = this.txtBuscaLivro.getText();
        
        int cbxIndex = this.comboBoxBuscaLivro.getSelectedIndex();
        DefaultTableModel tabela = (DefaultTableModel) tabelaListaLivro.getModel();
        limparTabela();
        for(int i = 0; i < livros.quantidadeLivro(); i++){
            livro = livros.getLivro(i);
            if(cbxIndex == 0){
                if(livro.getTitulo().toLowerCase().contains(txtDeBusca))
                    inserirLinhaTabela(livro, tabela);
            }else if(cbxIndex == 1){
                if(livro.getAutor().toLowerCase().contains(txtDeBusca))
                    inserirLinhaTabela(livro, tabela);
            }else if(cbxIndex == 2){
                if(livro.getIsbn().contains(txtDeBusca))
                    inserirLinhaTabela(livro, tabela);
            }else if(cbxIndex == 3){
                if(livro.getQuantidadeDisponivel() > 0)
                    inserirLinhaTabela(livro, tabela);
            }
        }
         if(tabelaListaLivro.getRowCount() == 0 && buscar)
            JOptionPane.showMessageDialog(this, "Nenhum resultado encontrado", "Livro não Encontrado", JOptionPane.WARNING_MESSAGE);
     }   
