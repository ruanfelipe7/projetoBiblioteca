    private void btnSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSalvarActionPerformed
        // TODO add your handling code here:
        String nome = txtNomeInfoBibliot.getText();
        String cpf = txtCPFInfoBibliot.getText();
        String telefone = txtTelefoneInfoBibliot.getText();
        String rg = txtRGInfoBibliot.getText();
        String dataNasc = txtDataNascInfoBibliot.getText();
        String usuario = txtUsuarioInfoBibliot.getText();
        String senha = txtSenhaInfoBibliot.getText();
        boolean testeCampos = true;
        boolean alterarLabel = false;

        if(nome.equals("")){
            testeCampos = false;
            txtNomeInfoBibliot.setBackground(Color.decode("#FF6347"));
        }
        if(cpf.equals("   .   .   -  ")){
            testeCampos = false;
            txtCPFInfoBibliot.setBackground(Color.decode("#FF6347"));
        }
        if(telefone.equals("(  )      -    ")){
            testeCampos = false;
            txtTelefoneInfoBibliot.setBackground(Color.decode("#FF6347"));

        }
        if(rg.equals("          - ")){
            testeCampos = false;
            txtRGInfoBibliot.setBackground(Color.decode("#FF6347"));
        }
        if(dataNasc.equals("")){
            testeCampos = false;
            txtDataNascInfoBibliot.setBackground(Color.decode("#FF6347"));
        }
        if(usuario.equals("")){
            testeCampos = false;
            txtUsuarioInfoBibliot.setBackground(Color.decode("#FF6347"));
        }
        if(senha.equals("")){
            testeCampos = false;
            txtSenhaInfoBibliot.setBackground(Color.decode("#FF6347"));
        }

        if(!testeCampos){
            JOptionPane.showMessageDialog(this, "Preencha todos os campos obrigatórios", "Erro ao realizar o cadastro", JOptionPane.ERROR_MESSAGE);
            txtNomeInfoBibliot.requestFocus();
            pintarCampos();
            return;
        }

        DateTimeFormatter formato = DateTimeFormatter.ofPattern("dd/MM/yyyy");
        LocalDate dataNascimento = LocalDate.parse(dataNasc, formato);

        if(bibliotecarias.bibliotecariaJaCadastrada(usuario, cpf, this.bibliotecaria)){
            JOptionPane.showMessageDialog(this, "Registro já Encontrado", "Erro ao Atualizar Cadastro", JOptionPane.ERROR_MESSAGE);
            limparCampos();
            txtNomeInfoBibliot.requestFocus();
            inicializaCampos(bibliotecaria);
            return;
        }
        
        if(bibliotecaria.getUsuario().equals(usuarioSessao)){
            alterarLabel = true;
            
        }

        bibliotecaria.setNome(nome);
        bibliotecaria.setCpf(cpf);
        bibliotecaria.setRg(rg);
        bibliotecaria.setDataNascimento(dataNascimento);
        bibliotecaria.setUsuario(usuario);
        bibliotecaria.setSenha(senha);
        
        JOptionPane.showMessageDialog(this, "Bibliotecaria Atualizada com Sucesso", "Alteração realizada", JOptionPane.INFORMATION_MESSAGE);
        
        
        
        limparCampos();
        if(!isPerfil){
            TelaPesquisaBibliotecaria pesquisaBibliotecaria = new TelaPesquisaBibliotecaria(this.parent, false);
            pesquisaBibliotecaria.setVisible(true);
            pesquisaBibliotecaria.setLocationRelativeTo(null);
        }
        dispose();
        this.parent.enable();
        if(alterarLabel){
            TelaLogin login = new TelaLogin();
            login.loginGuest(bibliotecaria.getUsuario(), bibliotecaria.getSenha(), 1);
            this.parent.dispose();
        }    
    }//GEN-LAST:event_btnSalvarActionPerformed

