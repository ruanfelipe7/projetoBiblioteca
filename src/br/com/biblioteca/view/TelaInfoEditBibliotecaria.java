    public void limparCampos(){
        txtNomeInfoBibliot.setText("");
        txtCPFInfoBibliot.setText("");
        txtTelefoneInfoBibliot.setText("");
        txtRGInfoBibliot.setText("");
        txtDataNascInfoBibliot.setText("");
        txtUsuarioInfoBibliot.setText("");
        txtSenhaInfoBibliot.setText("");
    }
    
    public void pintarCampos(){
        txtNomeInfoBibliot.setBackground(Color.WHITE);
        txtCPFInfoBibliot.setBackground(Color.WHITE);
        txtTelefoneInfoBibliot.setBackground(Color.WHITE);
        txtRGInfoBibliot.setBackground(Color.WHITE);
        txtDataNascInfoBibliot.setBackground(Color.WHITE);
        txtUsuarioInfoBibliot.setBackground(Color.WHITE);
        txtSenhaInfoBibliot.setBackground(Color.WHITE);
    }
    
    public void inicializaCampos(Bibliotecaria bibliotecaria){
        txtNomeInfoBibliot.setText(bibliotecaria.getNome());
        txtCPFInfoBibliot.setText(bibliotecaria.getCpf());
        txtTelefoneInfoBibliot.setText(bibliotecaria.getTelefone());
        txtRGInfoBibliot.setText(bibliotecaria.getRg());
        txtUsuarioInfoBibliot.setText(bibliotecaria.getUsuario());
        txtSenhaInfoBibliot.setText(bibliotecaria.getSenha());
        
        String data = String.valueOf(bibliotecaria.getDataNascimento());
        String camposData[] = data.split("-");
        String dataNascimento = camposData[2]+"/"+camposData[1]+"/"+camposData[0];
        txtDataNascInfoBibliot.setText(dataNascimento);
    }
    
    public void bloqueiaCampos(){
        txtNomeInfoBibliot.setEditable(false);
        txtCPFInfoBibliot.setEditable(false);
        txtTelefoneInfoBibliot.setEditable(false);
        txtRGInfoBibliot.setEditable(false);
        txtDataNascInfoBibliot.setEditable(false);
        txtUsuarioInfoBibliot.setEditable(false);
        txtSenhaInfoBibliot.setEditable(false);
        btnSalvar.setEnabled(false);
    }

