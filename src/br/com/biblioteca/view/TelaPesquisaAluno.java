    private void btnExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnExcluirActionPerformed
        // TODO add your handling code here:
        int linhaSelecionada = tabelaListaAluno.getSelectedRow();
            if(linhaSelecionada == -1){
                JOptionPane.showMessageDialog(this, "Nenhum Aluno Selecionado", "Falha na Exclusão", JOptionPane.WARNING_MESSAGE);
                return;
            }
            removerAluno(linhaSelecionada);
            
            limparTabela();
            txtBuscaAluno.setText("");
            buscarAluno(false);
    }//GEN-LAST:event_btnExcluirActionPerformed

    private void removerAluno(int index){
        int decisaodeExcluir = 0;
        Aluno aluno = new Aluno();
           try{
                Object obj = tabelaListaAluno.getValueAt(index, 1);
                String matricula = String.valueOf(obj);
                int matricula_aluno = Integer.parseInt(matricula);
                aluno = alunos.getAlunoMatricula(matricula_aluno);
                decisaodeExcluir = JOptionPane.showOptionDialog(this, "Deseja Realmente Excluir o Aluno " + aluno.getNome() , "Decisão de Exclusão", JOptionPane.YES_OPTION, JOptionPane.NO_OPTION, null, null, null);
                if(decisaodeExcluir == 0){
                    if(aluno.getQuantidadeLivrosAlugados() > 0){
                        JOptionPane.showMessageDialog(this, "O aluno possui livros alugados", "Erro ao Realizar a Exclusão", JOptionPane.ERROR_MESSAGE);
                        return;
                    }
                    alunos.removerAluno(aluno);
                    JOptionPane.showMessageDialog(this, "Aluno Excluido", "Exclusão Efetuada", JOptionPane.INFORMATION_MESSAGE);
                }
           }catch(NullPointerException ex){
               JOptionPane.showMessageDialog(this, "Erro ao Realizar a Exclusão", "Erro", JOptionPane.ERROR_MESSAGE);
           }

           
    }
    
     
    
