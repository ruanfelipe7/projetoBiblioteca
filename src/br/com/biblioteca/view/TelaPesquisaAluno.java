    private void btnInformacoesActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnInformacoesActionPerformed
        // TODO add your handling code here:
        int linhaSelecionada = tabelaListaAluno.getSelectedRow();
        if(linhaSelecionada == -1){
                JOptionPane.showMessageDialog(this, "Nenhum Aluno Selecionado", "Falha ao Ver as Informações", JOptionPane.WARNING_MESSAGE);
                return;
        }
        Aluno aluno = new Aluno();
        try{
            Object obj = tabelaListaAluno.getValueAt(linhaSelecionada, 1);
            String matricula = String.valueOf(obj);
            int matricula_aluno = Integer.parseInt(matricula);
            aluno = alunos.getAlunoMatricula(matricula_aluno);
            TelaInfoEditAluno telaInfoEditAluno = new TelaInfoEditAluno(this.parent, false, aluno, false, false);
            telaInfoEditAluno.setVisible(true);
            telaInfoEditAluno.setLocationRelativeTo(null);
            dispose();
        }catch(NullPointerException ex){
            JOptionPane.showMessageDialog(this, "Erro ao Selecionar o Aluno", "Erro", JOptionPane.ERROR_MESSAGE);
        }
        
        
        
    }//GEN-LAST:event_btnInformacoesActionPerformed

