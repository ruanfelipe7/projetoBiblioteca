    public void inserirLinhaTabela(Aluno aluno, DefaultTableModel tabela){
        tabela.addRow(new Object[] {aluno.getNome(), String.valueOf(aluno.getMatricula()), aluno.getCurso(), aluno.getCpf(), String.valueOf(aluno.getQuantidadeLivrosAlugados())});
    }
    
    public void limparTabela(){
        DefaultTableModel tabela = (DefaultTableModel) tabelaListaAluno.getModel();
        while(tabela.getRowCount() > 0){
            tabela.removeRow(0);
        }
    }
    
    private void buscarAluno(boolean buscar){
        boolean flagEncontrouAluno = false;
        Aluno aluno = new Aluno();
        String txtDeBusca = this.txtBuscaAluno.getText();
        String matricula;
        String quantidadeLivros;
        int cbxIndex = this.comboBoxBusca.getSelectedIndex();
        DefaultTableModel tabela = (DefaultTableModel) tabelaListaAluno.getModel();
        limparTabela();
        for(int i = 0; i < alunos.quantidadeAlunos(); i++){
            aluno = alunos.getAluno(i);
            if(cbxIndex == 0){
                if(aluno.getNome().toLowerCase().contains(txtDeBusca))
                    inserirLinhaTabela(aluno, tabela);
            }else if(cbxIndex == 1){
                matricula = String.valueOf(aluno.getMatricula());
                if(matricula.contains(txtDeBusca))
                    inserirLinhaTabela(aluno, tabela);
            }else if(cbxIndex == 2){
                quantidadeLivros = String.valueOf(aluno.getQuantidadeLivrosAlugados());
                if(aluno.getQuantidadeLivrosAlugados() > 0)
                    inserirLinhaTabela(aluno, tabela);
            }
        }
        
        if(tabelaListaAluno.getRowCount() == 0 && buscar)
            JOptionPane.showMessageDialog(this, "Nenhum resultado encontrado", "Aluno não Encontrado", JOptionPane.WARNING_MESSAGE);
    }
