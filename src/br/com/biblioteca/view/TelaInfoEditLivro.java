    private void btnSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSalvarActionPerformed
        // TODO add your handling code here:
        String titulo = txtTituloInfoLivro.getText();
        String autor = txtAutorInfoLivro.getText();
        String isbn = txtISBNInfoLivro.getText();
        String descricao = txtDescricaoInfoLivro.getText();
        String quantidade = String.valueOf(txtQuantidadeInfoLivro.getValue());

        boolean testeCampos = true;

        if(titulo.equals("")){
            testeCampos = false;
            txtTituloInfoLivro.setBackground(Color.decode("#FF6347"));
        }
        if(autor.equals("")){
            testeCampos = false;
            txtAutorInfoLivro.setBackground(Color.decode("#FF6347"));
        }
        if(isbn.equals("   -  -   -    - ")){
            testeCampos = false;
            txtISBNInfoLivro.setBackground(Color.decode("#FF6347"));
        }
        if(descricao.equals("")){
            testeCampos = false;
            txtDescricaoInfoLivro.setBackground(Color.decode("#FF6347"));
        }

        int quantidadeLivro = Integer.parseInt(quantidade);

        if(quantidadeLivro == 0){
            testeCampos = false;
            txtQuantidadeInfoLivro.setBackground(Color.decode("#FF6347"));
        }
        if(!testeCampos){
            JOptionPane.showMessageDialog(this, "Preencha todos os campos obrigatórios", "Erro ao realizar o cadastro", JOptionPane.ERROR_MESSAGE);
            //pintarCampos();
            return;
        }
        
        
//        String caminho = getClass().getResource("../images/").toString().substring(5);
//        Icon livroImage = lblImagem.getIcon();
//        System.out.println(caminho);
//        File outputfile = new File(caminho+nomeImagem);
//        BufferedImage image = null;
//        System.out.println(localImagem);
//
//        try {
//            File arquivo1 = new File(localImagem);
//            image = ImageIO.read(arquivo1);
//            ImageIO.write(image, "png", outputfile);
//        } catch (IOException ex) {
//            Logger.getLogger(TelaCadastroLivro.class.getName()).log(Level.SEVERE, null, ex);
//        }
        
        
        if(livros.livroJaCadastrado(isbn, livro)){
            JOptionPane.showMessageDialog(this, "Registro já Encontrado", "Erro ao Realizar Atualização", JOptionPane.ERROR_MESSAGE);
            limparCampos();
            txtTituloInfoLivro.requestFocus();
            inicializaCampos(livro);
            return;
        }
        
        
        livro.setTitulo(titulo);
        livro.setAutor(autor);
        livro.setIsbn(isbn);
        livro.setQuantidade(quantidadeLivro);
        livro.setDescricao(descricao);
        livro.setImagemLivro(livro.getImagemLivro());
        livro.setNomeImagem(nomeImagem);;
        livro.setDisponivel(livro.getQuantidade() - 1);

        JOptionPane.showMessageDialog(this, "Livro Atualizado com Sucesso", "Alteração realizada", JOptionPane.INFORMATION_MESSAGE);
        limparCampos();
        TelaPesquisaLivro pesquisaLivro = new TelaPesquisaLivro(this.parent, false);
        pesquisaLivro.setVisible(true);
        pesquisaLivro.setLocationRelativeTo(null);
        dispose();

    }//GEN-LAST:event_btnSalvarActionPerformed

