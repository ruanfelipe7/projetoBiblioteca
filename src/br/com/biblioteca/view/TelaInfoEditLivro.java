    private void inicializaCampos(Livro livro){
        txtTituloInfoLivro.setText(livro.getTitulo());
        txtAutorInfoLivro.setText(livro.getAutor());
        txtISBNInfoLivro.setText(livro.getIsbn());
        txtQuantidadeInfoLivro.setValue(livro.getQuantidade());
        txtDisponivelInfoLivro.setText(String.valueOf(livro.getQuantidadeDisponivel()));
        txtDescricaoInfoLivro.setText(livro.getDescricao());
        lblImagem.setIcon(livro.getImagemLivro());
        this.nomeImagem = nomeImagem;
        this.localImagem = getClass().getResource("../images/").toString().substring(5);
    }
    
    private void bloqueiaCampos(){
        txtTituloInfoLivro.setEditable(false);
        txtAutorInfoLivro.setEditable(false);
        txtISBNInfoLivro.setEditable(false);
        txtQuantidadeInfoLivro.setEnabled(false);
        txtDescricaoInfoLivro.setEditable(false);
        //lblImagem.setEnabled(false);
        btnSalvar.setEnabled(false);
    }
    
    private void limparCampos(){
        txtTituloInfoLivro.setText("");
        txtAutorInfoLivro.setText("");
        txtISBNInfoLivro.setText("");
        txtQuantidadeInfoLivro.setValue(0);
        txtDescricaoInfoLivro.setText("");
    }
