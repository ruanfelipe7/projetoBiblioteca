    public void inicializaCampos(Aluno aluno){
        txtNomeInfoAluno.setText(aluno.getNome());
        txtCPFInfoAluno.setText(aluno.getCpf());
        txtTelefoneInfoAluno.setText(aluno.getTelefone());
        txtLivrosInfoAluno.setText(String.valueOf(aluno.getQuantidadeLivrosAlugados()));
        txtMatriculaInfoAluno.setText(String.valueOf(aluno.getMatricula()));
        txtCursoInfoAluno.setText(aluno.getCurso());
        txtUsuarioInfoAluno.setText(aluno.getUsuario());
        txtSenhaInfoAluno.setText(aluno.getSenha());
        
        String data = String.valueOf(aluno.getDataNascimento());
        String camposData[] = data.split("-");
        String dataNascimento = camposData[2]+"/"+camposData[1]+"/"+camposData[0];
        txtDataNascInfoAluno.setText(dataNascimento);
    }
    
