    private void btnSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSalvarActionPerformed
        // TODO add your handling code here:
        String nome = txtNomeInfoAluno.getText();
        String cpf = txtCPFInfoAluno.getText();
        String telefone = txtTelefoneInfoAluno.getText();
        String matricula = txtMatriculaInfoAluno.getText();
        String dataNasc = txtDataNascInfoAluno.getText();
        String curso = txtCursoInfoAluno.getText();
        String usuario = txtUsuarioInfoAluno.getText();
        String senha = txtSenhaInfoAluno.getText();
        String quantidadeLivros = txtLivrosInfoAluno.getText();
        boolean testeCampos = true;
        boolean alterarLabel = false;

        if(nome.equals("")){
            testeCampos = false;
            txtNomeInfoAluno.setBackground(Color.decode("#FF6347"));
        }
        if(cpf.equals("   .   .   -  ")){
            testeCampos = false;
            txtCPFInfoAluno.setBackground(Color.decode("#FF6347"));
        }
        if(telefone.equals("(  )      -    ")){
            testeCampos = false;
            txtTelefoneInfoAluno.setBackground(Color.decode("#FF6347"));

        }
        if(matricula.equals("      ")){
            testeCampos = false;
            txtMatriculaInfoAluno.setBackground(Color.decode("#FF6347"));
        }
        if(dataNasc.equals("")){
            testeCampos = false;
            txtDataNascInfoAluno.setBackground(Color.decode("#FF6347"));
        }
        if(curso.equals("")){
            testeCampos = false;
            txtCursoInfoAluno.setBackground(Color.decode("#FF6347"));
        }
        if(usuario.equals("")){
            testeCampos = false;
            txtUsuarioInfoAluno.setBackground(Color.decode("#FF6347"));
        }
        if(senha.equals("")){
            testeCampos = false;
            txtSenhaInfoAluno.setBackground(Color.decode("#FF6347"));
        }

        if(!testeCampos){
            JOptionPane.showMessageDialog(this, "Preencha todos os campos obrigatórios", "Erro ao Atualizar", JOptionPane.ERROR_MESSAGE);
            txtNomeInfoAluno.requestFocus();
            pintarCampos();
            return;
        }

        int matrciula_aluno = Integer.parseInt(matricula);
        DateTimeFormatter formato = DateTimeFormatter.ofPattern("dd/MM/yyyy");
        LocalDate dataNascimento = LocalDate.parse(dataNasc, formato);
     
        if(alunos.alunoJaCadastrado(usuario, matrciula_aluno, this.aluno)){
            JOptionPane.showMessageDialog(this, "Registro já Encontrado", "Erro ao Atualizar Cadastro", JOptionPane.ERROR_MESSAGE);
            limparCampos();
            txtNomeInfoAluno.requestFocus();
            inicializaCampos(aluno);
            return;
        }
        
        if(aluno.getUsuario().equals(usuarioSessao)){
            alterarLabel = true;
        }
        
        aluno.setNome(nome);
        aluno.setCpf(cpf);
        aluno.setDataNascimento(dataNascimento);
        aluno.setTelefone(telefone);
        aluno.setMatricula(matrciula_aluno);
        aluno.setCurso(curso);
        aluno.setUsuario(usuario);
        aluno.setSenha(senha);
        
        JOptionPane.showMessageDialog(this, "Aluno Atualizado com Sucesso", "Alteração Realizada", JOptionPane.INFORMATION_MESSAGE);
         
        limparCampos();
        if(!isPerfil){
            TelaPesquisaAluno pesquisaAluno = new TelaPesquisaAluno(this.parent, false);
            pesquisaAluno.setVisible(true);
            pesquisaAluno.setLocationRelativeTo(null);
        }
        dispose();
        this.parent.enable();
        if(alterarLabel){
            TelaLogin login = new TelaLogin();
            login.loginGuest(aluno.getUsuario(), aluno.getSenha(), 0);
            this.parent.dispose();
        }
    }//GEN-LAST:event_btnSalvarActionPerformed

